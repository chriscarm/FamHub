#!/usr/bin/env node
/**
 * Creates placeholder asset images for Expo
 * Run with: node scripts/setup-assets.js
 */

const fs = require('fs');
const path = require('path');

// Simple 1x1 pixel PNG (dark background)
const PLACEHOLDER_PNG = Buffer.from([
  0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a, 0x00, 0x00, 0x00, 0x0d,
  0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
  0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53, 0xde, 0x00, 0x00, 0x00,
  0x0c, 0x49, 0x44, 0x41, 0x54, 0x08, 0xd7, 0x63, 0x08, 0x08, 0x08, 0x00,
  0x00, 0x04, 0x00, 0x01, 0x6a, 0x2c, 0x97, 0xca, 0x00, 0x00, 0x00, 0x00,
  0x49, 0x45, 0x4e, 0x44, 0xae, 0x42, 0x60, 0x82
]);

const assetsDir = path.join(__dirname, '..', 'assets');

const files = ['icon.png', 'splash.png', 'adaptive-icon.png'];

files.forEach(file => {
  const filePath = path.join(assetsDir, file);
  if (!fs.existsSync(filePath)) {
    fs.writeFileSync(filePath, PLACEHOLDER_PNG);
    console.log(`Created placeholder: ${file}`);
  } else {
    console.log(`Skipped (exists): ${file}`);
  }
});

console.log('\nPlaceholder assets created!');
console.log('Replace these with proper app icons before publishing.');
